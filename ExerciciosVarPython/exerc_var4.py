# Obtenha um valor em metros via entrada do usuário e o exiba convertido em milímetros (apresente o valor para o usuário sem nenhuma casa decimal).
# Exemplo: entrada → 1.5 ; saída → 1500mm

m = float(input("Digite o valor em metros: exemplo 1.5, Digite aqui:  "))
print(f"O valor em milímetros é {m:.3f} ")
