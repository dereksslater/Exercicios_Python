# Recebe uma lista de números e apresenta para o usuário o fatorial de cada um deles (utilize a função factorial do módulo math). 
import math

def lista_fatorial(lista):
    ind = 0
    lista[ind]
    while ind < len(lista):
        total = math.factorial(lista[ind])
        print(total)
        ind = ind + 1
        
          

print(lista_fatorial([3,4,5,7,5,5,2,5,2,1,1,0]))
